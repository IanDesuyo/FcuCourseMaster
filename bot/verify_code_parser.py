def parse_veify_code(image: bytes):
    """
    Parse verify code from image.
    Source: https://www.dcard.tw/f/fcu/p/236946822

    Args:
        image (bytes): Image bytes.

    Returns:
        str: Verify code.
    """
    code = ""
    image = cv2.imdecode(np.frombuffer(image, np.uint8), cv2.IMREAD_COLOR)
    for i in range(4):  # 4 digits
        pix = image[5:17, 6 + 9 * i : 14 + 9 * i]
        r = 0
        t = 255
        n = []
        pix_1d = pix.flatten()
        for j in range(0, pix.size, 3):
            n.append(int(pix_1d[j + 2]) + int(pix_1d[j + 0]) > 350)

        for j, digit in enumerate(DIGITS):
            s = 0
            for k in range(len(digit)):
                s += n[k] ^ digit[k]

            if s < t:
                t = s
                r = j

        code += str(r)

    return code
